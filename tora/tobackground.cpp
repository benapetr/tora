toBackground::toBackground(QObject* parent,const char* name)
  : toTimer(parent,name)
{
  if (!Animation)
    init();
}

void toBackground::start(int msec)
{
  if (!Animation)
    init();
  if (!isActive()) {
    Running++;
    if (!Animation)
      return;
    Animation->unpause();
  }
  Animation->setSpeed(Running*100);
  if (Running>1)
    QToolTip::add(Label,tr("%1 queries running in background.").arg(Running));
  else
    QToolTip::add(Label,tr("One query running in background."));
  toTimer::start(msec);
}

void toBackground::stop(void)
{
  if (!Animation)
    init();
  if (isActive()) {
    Running--;
    if (Animation) {
      if (Running==0)
	Animation->pause();
      else
	Animation->setSpeed(Running*100);
      if (Running>1)
	QToolTip::add(Label,tr("%1 queries running in background.").arg(Running));
      else if (Running==1)
	QToolTip::add(Label,tr("One query running in background."));
      else
	QToolTip::add(Label,tr("No background queries."));
    }
  }
  toTimer::stop();
}

void toBackground::init(void)
{
  toMain *main=toMainWidget();
  if (!main)
    return;

  QByteArray arr;
  arr.assign(data,data_length);
  Animation=new QMovie(arr);
  Animation->pause();
  Label=new toBackgroundLabel(main->statusBar());
  Label->setMovie(*Animation);
  Label->show();
  main->statusBar()->addWidget(Label,0,true);
  QToolTip::add(Label,tr("No background queries."));
}

toBackground::~toBackground()
{
  stop();
}
